// Function to check if a tile index is walkable
inline static bool isTileWalkable(uint8_t tileIndex) {
    for (uint8_t i = 0; i < maxWalkableTiles; ++i) {
        if (walkableTiles[i] == tileIndex) {
            return true;
        }
    }
    return false;
}

struct Warp {
    uint8_t x;
    uint8_t y;
    uint8_t warpTo;
    uint8_t px;
    uint8_t py;
    uint8_t dir;
};

struct Levels {
    uint8_t id;
    uint8_t mapSizeX;
    uint8_t mapSizeY;
    Warp warps[4];
    uint8_t numWarps;
};

